import androidx.compose.material3.Text
import androidx.compose.runtime.Composable
import androidx.compose.ui.Alignment
import androidx.compose.ui.Modifier
import androidx.compose.ui.text.style.TextAlign
import androidx.compose.ui.unit.dp

/**
 * Componente que muestra un estado vacÃ­o cuando no hay resultados
 *
 * @param message Mensaje a mostrar
 * @param onClearFilters Callback opcional para limpiar filtros
 * @param modifier Modificador para personalizar el contenedor
 */
@Composable
fun EmptyState(
    message: String = "No games found",
    onClearFilters: (() -> Unit)? = null,
    modifier: Modifier = Modifier
) {
    Column(
        modifier = modifier
            .fillMaxSize()
            .padding(32.dp),
        horizontalAlignment = Alignment.CenterHorizontally,
        verticalArrangement = Arrangement.Center
    ) {
        Icon(
            imageVector = Icons.Default.SearchOff,
            contentDescription = null,
            modifier = Modifier.size(64.dp),
            tint = MaterialTheme.colorScheme.onSurfaceVariant
        )

        Spacer(modifier = Modifier.height(16.dp))

        Text(
            text = message,
            style = MaterialTheme.typography.bodyLarge,
            textAlign = TextAlign.Center,
            color = MaterialTheme.colorScheme.onSurfaceVariant
        )

        onClearFilters?.let {
            Spacer(modifier = Modifier.height(16.dp))
            RoundedButton(
                texto = "Clear Filters",
                onClick = it
            )
        }
    }
}
